#include <stdio.h>

int main(int ac, char **av) {
	if (ac != 2) {
		printf("Arg error\n");
		return (1);
	}
	for (int i = 1; i < ac; i++) {
		printf("%s\n", av[i]);
	}
	return (0);
}
